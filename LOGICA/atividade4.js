let a = 10;
let b = 20;

let andResult = (a > 5 && b > 15);
console.log("Resultado do AND (a > 5 && b > 15):", andResult);

let orResult = (a > 5 || b < 10);
console.log("Resultado do OR (a > 5 || b < 10):", orResult);

let notResult = !(a < 15); 
console.log("Resultado do NOT !(a < 15):", notResult);
